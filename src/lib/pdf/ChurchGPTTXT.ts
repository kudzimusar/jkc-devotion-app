function buildAdminDocumentTXT(d: Record<string, unknown>, orgName: string, date: string): string {
  const sections = Array.isArray(d.sections) ? d.sections as Record<string, unknown>[] : []
  const lines: string[] = [
    header(orgName, String(d.title ?? 'Document')),
    meta('Date', String(d.date ?? date)),
    meta('Author', String(d.author ?? '')),
    meta('Reference', String(d.reference ?? '')),
    '',
    bodyText(String(d.summary ?? '')),
    ...sections.flatMap(sec => [
      section(String(sec.heading ?? '')),
      bodyText(String(sec.content ?? '')),
    ]),
    footer(date),
  ]
  return lines.join('\n')
}

// ── Public API ────────────────────────────────────────────────────────────────

export function buildTXT(documentData: Record<string, unknown>, orgName = 'Church OS'): string {
  const date = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })
  const type = String(documentData.type ?? '')

  switch (type) {
    case 'sermon-outline':        return buildSermonOutlineTXT(documentData, orgName, date)
    case 'service-order':         return buildServiceOrderTXT(documentData, orgName, date)
    case 'event-brief':           return buildEventBriefTXT(documentData, orgName, date)
    case 'stewardship-campaign':  return buildStewardshipCampaignTXT(documentData, orgName, date)
    case 'bible-study-guide':     return buildBibleStudyGuideTXT(documentData, orgName, date)
    case 'small-group-guide':     return buildSmallGroupGuideTXT(documentData, orgName, date)
    case 'youth-lesson':          return buildYouthLessonTXT(documentData, orgName, date)
    default:                      return buildAdminDocumentTXT(documentData, orgName, date)
  }
}
