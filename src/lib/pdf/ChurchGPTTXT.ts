// Plain-text document builder — no external deps

const WIDE = '═'.repeat(68)
const THIN = '─'.repeat(68)

function center(text: string, width = 68): string {
  const pad = Math.max(0, Math.floor((width - text.length) / 2))
  return ' '.repeat(pad) + text
}

function header(orgName: string, subtitle: string): string {
  return [
    WIDE,
    center(orgName),
    center('Generated by ChurchGPT · Church OS'),
    THIN,
    center(subtitle),
    WIDE,
    '',
  ].join('\n')
}

function meta(label: string, value: string): string {
  return value ? `${label.padEnd(22)}: ${value}` : ''
}

function section(title: string): string {
  return `\n${THIN}\n  ${title.toUpperCase()}\n${THIN}\n`
}

function bodyText(text: string): string {
  if (!text) return ''
  const words = text.split(' ')
  const lines: string[] = []
  let current = ''
  for (const word of words) {
    if (current.length + word.length + 1 > 68) {
      lines.push(current)
      current = word
    } else {
      current = current ? `${current} ${word}` : word
    }
  }
  if (current) lines.push(current)
  return lines.join('\n')
}

function bullet(text: string): string {
  return `  • ${text}`
}

function callout(text: string): string {
  return text ? `  | ${text.trim().replace(/\n/g, '\n  | ')}` : ''
}

function footer(date: string): string {
  return `\n${WIDE}\n${center(`ChurchGPT · Church OS · ${date} · Confidential`)}\n${WIDE}`
}

