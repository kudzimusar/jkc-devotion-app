function buildSermonOutlineTXT(d: Record<string, unknown>, orgName: string, date: string): string {
  const points = Array.isArray(d.main_points) ? d.main_points as Record<string, unknown>[] : []
  const illustrations = Array.isArray(d.illustrations) ? d.illustrations as string[] : []
  const lines: string[] = [
    header(orgName, String(d.title ?? 'Sermon Outline')),
    meta('Scripture', String(d.anchor_text ?? '')),
    meta('Big Idea', String(d.big_idea ?? '')),
    meta('Context', String(d.context ?? '')),
    section('Main Points'),
    ...points.flatMap((pt, i) => [
      `${i + 1}. ${String(pt.title ?? '')}`,
      bodyText(String(pt.content ?? '')),
      callout(String(pt.scripture ?? '')),
      '',
    ]),
    section('Illustrations & Stories'),
    ...illustrations.map(ill => bullet(ill)),
    section('Application'),
    bodyText(String(d.application ?? '')),
    section('Invitation / Altar Call'),
    callout(String(d.invitation ?? '')),
    footer(date),
  ]
  return lines.filter(l => l !== null && l !== undefined).join('\n')
}

function buildServiceOrderTXT(d: Record<string, unknown>, orgName: string, date: string): string {
  const items = Array.isArray(d.order_items) ? d.order_items as Record<string, unknown>[] : []
  const team = Array.isArray(d.team) ? d.team as Record<string, unknown>[] : []
  const lines: string[] = [
    header(orgName, String(d.title ?? 'Service Order')),
    meta('Date / Time', String(d.date_time ?? '')),
    meta('Theme', String(d.theme ?? '')),
    meta('Scripture Focus', String(d.scripture_focus ?? '')),
    section('Order of Service'),
    ...items.map(item =>
      `  ${String(item.time ?? '').padEnd(10)} ${String(item.element ?? '')}${item.leader ? ` (${String(item.leader)})` : ''}`
    ),
    section('Team Assignments'),
    ...team.map(t => meta(String(t.role ?? ''), String(t.name ?? ''))),
    section('Notes'),
    bodyText(String(d.notes ?? '')),
    footer(date),
  ]
  return lines.join('\n')
}

